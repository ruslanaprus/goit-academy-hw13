package org.example.dto;

public class Task {
    int userId;
    int id;
    String title;
    boolean completed;

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }


    public boolean isCompleted() {
        return completed;
    }

}
